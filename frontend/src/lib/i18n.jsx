const reverseAutoTranslations = Object.fromEntries(
  Object.entries(autoTranslations).map(([english, vietnamese]) => [vietnamese, english])
);

const translateLiteral = (value, language) => {
  if (typeof value !== "string" || value.trim().length === 0) return value;
  const trimmed = value.trim();
  const leading = value.match(/^\s*/)?.[0] || "";
  const trailing = value.match(/\s*$/)?.[0] || "";
  const source = language === "vi" ? autoTranslations : reverseAutoTranslations;

  if (source[trimmed]) {
    return `${leading}${source[trimmed]}${trailing}`;
  }

  if (language === "vi") {
    const jobsFoundMatch = trimmed.match(/^(\d+)\s+jobs\s+found(?:\s+for\s+(.+))?$/i);
    if (jobsFoundMatch) {
      const count = jobsFoundMatch[1];
      const searchFor = jobsFoundMatch[2] ? ` cho ${jobsFoundMatch[2]}` : "";
      return `${leading}Tìm thấy ${count} việc làm${searchFor}${trailing}`;
    }

    const pageMatch = trimmed.match(/^Page\s+(\d+)$/i);
    if (pageMatch) return `${leading}Trang ${pageMatch[1]}${trailing}`;

    const unreadMatch = trimmed.match(/^(\d+)\s+Unread$/i);
    if (unreadMatch) return `${leading}${unreadMatch[1]} chưa đọc${trailing}`;
  }

  if (language === "en") {
    const jobsFoundMatch = trimmed.match(/^Tìm thấy\s+(\d+)\s+việc\s*làm(?:\s+cho\s+(.+))?$/i);
    if (jobsFoundMatch) {
      const count = jobsFoundMatch[1];
      const searchFor = jobsFoundMatch[2] ? ` for ${jobsFoundMatch[2]}` : "";
      return `${leading}${count} jobs found${searchFor}${trailing}`;
    }

    const pageMatch = trimmed.match(/^Trang\s+(\d+)$/i);
    if (pageMatch) return `${leading}Page ${pageMatch[1]}${trailing}`;

    const unreadMatch = trimmed.match(/^(\d+)\s+chưa đọc$/i);
    if (unreadMatch) return `${leading}${unreadMatch[1]} Unread${trailing}`;
  }

  return value;
};

export { translateLiteral };

const I18nContext = createContext(null);

const getInitialLanguage = () => {
  const stored = localStorage.getItem(LANGUAGE_KEY);
  if (languages.some((language) => language.code === stored)) {
    return stored;
  }

  return navigator.language?.toLowerCase().startsWith("vi") ? "vi" : "en";
};

export const I18nProvider = ({ children }) => {
  const [language, setLanguageState] = useState(getInitialLanguage);

  const setLanguage = (nextLanguage) => {
    const normalized = languages.some((item) => item.code === nextLanguage) ? nextLanguage : "en";
    localStorage.setItem(LANGUAGE_KEY, normalized);
    setLanguageState(normalized);
  };

  const value = useMemo(() => {
    const dictionary = dictionaries[language] || dictionaries.en;
    return {
      language,
      setLanguage,
      t: (key) => dictionary[key] || dictionaries.en[key] || key,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

I18nProvider.propTypes = {
  children: PropTypes.node,
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
};
